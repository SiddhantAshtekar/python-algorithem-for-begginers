# Some more methods to add in our list 
# insert method
# how to join (concantenate) two lists
# extend method 
# Difference b/w append and extend methods

# insert method
fruits1=['mango','orange']
fruits1.insert(1,"grapes")
print(fruits1)
# extend method
fruits2=["apple","banana"]
fruits1.extend(fruits2)
print(fruits1)

# difference b/w append and extend
fruits1.append(fruits2)
print(fruits1)